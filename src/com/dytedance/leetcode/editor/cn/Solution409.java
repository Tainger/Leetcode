public class Solution409 {
    public int getLongestPalindromeSubseq(String s) {
        int n = s.length();
        if (0 == n) return 0;
        int[][] dp = new int[n][n];
        for (int[] row : dp) {
            Arrays.fill(row, 0);
        }
        for (int i = 0; i < n; i++) dp[i][i] = 1; // åˆå§‹åŒ–çŠ¶æ€
        for (int i = n - 1; i >= 0; i--) {
            for (int j = i + 1; j < n; j++) {
                if (s.charAt(i) == s.charAt(j)) {
                    dp[i][j] = 2 + dp[i + 1][j - 1];
                } else {
                    dp[i][j] = Math.max(dp[i + 1][j], dp[i][j - 1]); // ä½œå‡ºè¿›ä¸€æ­¥å†³ç­–
                }
            }
        }
        return dp[0][n - 1]; // è¾“å‡ºç­”æ¡ˆ
    }
}
